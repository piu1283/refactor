public class Performance {
  private String name;
  private int score;

  public String getName() {
    return name;
  }

  public void setName(String arg) {
    name = arg;
  }

  public int getScore() {
    return score;
  }

  public void setScore(String arg) {
    score = Integer.parseInt(arg);
  }
}
